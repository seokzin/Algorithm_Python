n = int(input())
s = input()

BASE = s[0] # 베이스 칼라 - 가독성을 위해 따로 변수화했다.
cnt = 1 # 1회 - 베이스 칼라 채우는 과정

for i in range(1, len(s)):
  if s[i] != BASE and s[i] != s[i-1]: # 베이스와 다른 덩어리를 만났을 때 cnt++
    cnt += 1

print(cnt)

