# 어떤 방법으로 칠해도 한번에 변하는 색상은 최대 2개뿐이다.
# 즉 띄엄띄엄 칠하기나 범위 넓게 칠하기나 결론이 같다.

# 1-1. BRBRB / RBRBR 양끝 색이 같을 때 -> 베이스 칼라: 양끝 색이 최적
# 1-2. BRBR 양끝 색이 다를 때 -> 베이스 칼라 상관없다.
# ==> 결론: 베이스칼라는 s[0]이면 된다.
